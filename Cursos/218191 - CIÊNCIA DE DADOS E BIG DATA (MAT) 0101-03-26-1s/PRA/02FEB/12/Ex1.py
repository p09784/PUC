resposta = int(input("Qual é a sua idade? "))

if resposta >= 18:
    print("Você é maior de idade.")
else:
    print("Você é menor de idade.")

# O código acima solicita ao usuário que insira sua idade e, em seguida, verifica se a idade é maior ou igual a 18 anos. Se for, o programa imprime "Você é maior de idade." Caso contrário, ele imprime "Você é menor de idade."